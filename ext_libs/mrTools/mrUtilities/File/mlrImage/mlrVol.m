% set the text boxes
if n == 1
  names = {'X','Y','Z','Volume','Receiver'};
  for i = 1:h.nDim
    % make the inc/dec textboxes
    gVol{sysNum}.hCoordTextbox(i) = makeTextboxIncDec(sysNum,coord(i),i,1,i);
    % get name for button
