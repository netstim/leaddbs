  % but we can't align 2D volumes (because interp3 will fail), so bail out if this is
  % an alignment volume
  if gVol{sysNum}.n >= 1
    disp(sprintf('(mlrVol:loadVolume) !!! Can not align 2D image: %s !!!',mlrImageArgFilename(filename)));
    return
  end
  h.nDim = 3;
